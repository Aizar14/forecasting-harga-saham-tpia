    history_y = pd.concat([
        history_y,
        pd.Series([test.iloc[i]], index=[test.index[i]])
    ])

    history_x = pd.concat([
        history_x,
        pd.Series([x_test.iloc[i]], index=[x_test.index[i]])
    ])

