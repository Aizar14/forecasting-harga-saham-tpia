x_test = x[-60:]

print("Jumlah data train :", len(train))
print("Jumlah data test  :", len(test))

"""4.2 Baseline Model — Naive Forecast"""

import numpy as np

naive_pred = []

history = train.copy()

for actual in test:

    forecast = history.iloc[-1]

